	.text
	.globl	microblaze_enable_interrupts
	.ent	microblaze_enable_interrupts
	.align	2
microblaze_enable_interrupts:
	addi	r1, r1, -4
	swi	r12, r1, 0
	mfs	r12, rmsr
	ori	r12, r12, 2
	mts	rmsr, r12
	lwi	r12, r1, 0
	rtsd	r15, 8
	addi	r1, r1, 4
	.end	microblaze_enable_interrupts
